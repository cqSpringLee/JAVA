package com.lc.struct;

public class switchDemo02 {
    public static void main(String[] args) {
        String name = "xixi";
        switch (name){
            case "xixi":
                System.out.println("ok");
                break;
            case "xix":
                System.out.println("o");
                break;
            case "xii":
                System.out.println("k");
                break;
            default:
                System.out.println("bye");
        }
    }
}
